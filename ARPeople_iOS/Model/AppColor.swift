import Foundation
import UIKit

struct AppColor {
    
    /// #FFFFFF
    static var white: UIColor { return #colorLiteral(red: 1, green: 1, blue: 1, alpha: 1) }
    
    /// #000000
    static var black: UIColor { return #colorLiteral(red: 0, green: 0, blue: 0, alpha: 1) }
    
    /// #777777
    static var glay: UIColor { return #colorLiteral(red: 0.4666666667, green: 0.4666666667, blue: 0.4666666667, alpha: 1) }
    
    /// #E9E9E9
    static var bgGlay: UIColor { return #colorLiteral(red: 0.9137254902, green: 0.9137254902, blue: 0.9137254902, alpha: 1) }
    
    /// #64BBFF
    static var main: UIColor { return UIColor(hex: "64BBFF") }
    
    /// #FFFC87
    static var yellow: UIColor { return UIColor(hex: "FFFC87") }
}
